sex_job_income <- welfare %>%
  filter(!is.na(income)) %>%
  group_by(job, sex) %>%
  summarise(mean_income = mean(income))

top10 <- sex_job_income %>%
  arrange(desc(mean_income)) %>%
  head(10)

ggplot(data = top10, aes(x = reorder(job, mean_income), y = mean_income, fill = sex)) +
  geom_col(position = "dodge") +
  coord_flip()

